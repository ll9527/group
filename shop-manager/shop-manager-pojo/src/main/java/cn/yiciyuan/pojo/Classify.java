package cn.yiciyuan.pojo;

public class Classify {
	
	private Integer class_id;
	private String class_name;
	private Integer class_key;
	private Integer level;
	private Integer ondelect;
	private String images;
	public Integer getClass_id() {
		return class_id;
	}
	public void setClass_id(Integer class_id) {
		this.class_id = class_id;
	}
	public String getClass_name() {
		return class_name;
	}
	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}
	public Integer getClass_key() {
		return class_key;
	}
	public void setClass_key(Integer class_key) {
		this.class_key = class_key;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getOndelect() {
		return ondelect;
	}
	public void setOndelect(Integer ondelect) {
		this.ondelect = ondelect;
	}
	public String getImages() {
		return images;
	}
	public void setImages(String images) {
		this.images = images;
	}
	
}
